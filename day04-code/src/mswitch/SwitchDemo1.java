package mswitch;

public class SwitchDemo1 {
    public static void main(String[] args) {
        /**
         *格式
         */
        int a  =2;
        switch (a){

            case 1:
                System.out.println("春天");
                break;
            case 2:
                System.out.println("秋天");
                break;
            default:
                System.out.println("冬天");
                break;

        }
    }
}
