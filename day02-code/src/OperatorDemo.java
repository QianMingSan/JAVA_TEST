public class OperatorDemo {
    /**
     * 算数运算符
     *
     * +
     * -
     * *
     * /整数相除 结果只能得到整数
     * %：取余数
     *      场景 可以做奇数偶数判断 任何人 %2的结果 不是1（奇数） 就是0（偶数）
     * @param args
     */
    public static void main(String[] args) {
        //需要小数参与运算
        System.out.println(5.0/2);
        //取余
        System.out.println(5%2);

    }
}
