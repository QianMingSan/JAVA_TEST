public class ConstantDemo1 {
    /**
     * 字面量（别名常量）
     *      目标： 学习java中那些数据可以使用
     *
     *      分类：
     *      字符串 ：双引号所包裹的数据
     *      整数 ： 所有整数都可以用
     *      小数 ： 所有小数都可以用
     *      字符：单引号包裹 只能存放一个数据
     *      布尔: true false
     *      空：不允许直接打印
     *      ------------------------------------------------------
     * @param args
     */
    public static void main(String[] args) {
        //字符
        System.out.println("学习编程");
        //整数
        System.out.println(123);
        //小数
        System.out.println(45.6);
        //字符
        System.out.println('a');
        //布尔
        System.out.println(true);
        //布尔
        System.out.println(false);
    }
}
